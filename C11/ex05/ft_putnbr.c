#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	i;
	int		c;

	i = nb;
	if (i < 0)
	{
		i *= -1;
		write(1, "-", 1);
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
	{
		c = i + '0';
		write(1, &c, 1);
	}
}
