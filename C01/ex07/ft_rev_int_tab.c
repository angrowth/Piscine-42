void	ft_rev_int_tab(int *tab, int size)
{
	int	t[size];
	int i;

	i = 0;
	while (i < size)
	{
		t[i] = tab[size - i - 1];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = t[i];
		i++;
	}
}
